-- ---------- TABLAS BASE ----------
CREATE TABLE PROVINCIAS (
  ID_PROVINCIA NUMBER(2,0) NOT NULL,
  NOMBRE       VARCHAR2(25) NOT NULL,
  CONSTRAINT PROVINCIAS_PK PRIMARY KEY (ID_PROVINCIA)
);

CREATE TABLE ESTUDIANTES (
  NUMERO           NUMBER(5)   NOT NULL,
  APELLIDO         VARCHAR2(15) NOT NULL,
  NOMBRE           VARCHAR2(15) NOT NULL,
  FECHA_NACIMIENTO DATE         NOT NULL,
  CEDULA           VARCHAR2(15) NOT NULL,
  S_SOCIAL         VARCHAR2(15) NOT NULL,
  EMAIL            VARCHAR2(50),
  ID_PROVINCIA     NUMBER(2)    NOT NULL
);

CREATE TABLE CRITERIOS_DE_EVALUACION (
  CODIGO_NOTA CHAR(2)      NOT NULL,
  DESCRIPCION VARCHAR2(25) NOT NULL,
  CANTIDAD    NUMBER(3)    NOT NULL,
  PORCENTAJE  NUMBER(2)    NOT NULL
);

-- RUBRICA y ACTIVIDAD: aparecen en los queries de mejoras 5-7 del
-- documento del grupo anterior pero no estan en el Anexo II. Se
-- reconstruyen aqui a partir de esos queries.
CREATE TABLE RUBRICA (
  ID              NUMBER(5)   NOT NULL,
  CODIGO_RUBRICA  CHAR(2)     NOT NULL,
  NOMBRE          VARCHAR2(50) NOT NULL,
  DESCRIPCION     VARCHAR2(200),
  PORCENTAJE      NUMBER(3)   NOT NULL,
  CONSTRAINT RUBRICA_PK PRIMARY KEY (ID)
);

CREATE TABLE ACTIVIDAD (
  ID             NUMBER(5)    NOT NULL,
  NOMBRE         VARCHAR2(50) NOT NULL,
  DESCRIPCION    VARCHAR2(200),
  PUNTAJEMAXIMO  NUMBER(5,2),
  FECHAENTREGA   DATE,
  ID_RUBRICA     NUMBER(5),
  CONSTRAINT ACTIVIDAD_PK PRIMARY KEY (ID),
  CONSTRAINT ACT_RUB_FK FOREIGN KEY (ID_RUBRICA) REFERENCES RUBRICA(ID)
);

-- Tabla NOTAS ampliada con las columnas que usan las mejoras
CREATE TABLE NOTAS (
  ID_NOTA        NUMBER(10),
  NUMERO         NUMBER(5)  NOT NULL,
  CODIGO_NOTA    CHAR(2)    NOT NULL,
  FECHA          DATE       NOT NULL,
  VALOR_NOTA     NUMBER(5,2) NOT NULL,
  ID_ACTIVIDAD   NUMBER(5),
  CODIGO_RUBRICA CHAR(2),
  FECHA_REGISTRO DATE DEFAULT SYSDATE
);

CREATE TABLE ASISTENCIAS (
  NUMERO NUMBER(5)   NOT NULL,
  FECHA  DATE        NOT NULL,
  STATUS CHAR(1)     NOT NULL
);

-- ---------- CONSTRAINTS Y RELACIONES ----------
ALTER TABLE ESTUDIANTES
  ADD CONSTRAINT EST_KEY PRIMARY KEY (NUMERO);
ALTER TABLE ESTUDIANTES
  ADD CONSTRAINT EST_UK UNIQUE (CEDULA, S_SOCIAL);
ALTER TABLE ESTUDIANTES
  ADD CONSTRAINT ESTUDIANTES_FK_PROV FOREIGN KEY (ID_PROVINCIA)
      REFERENCES PROVINCIAS (ID_PROVINCIA);

ALTER TABLE CRITERIOS_DE_EVALUACION
  ADD CONSTRAINT DIST_KEY PRIMARY KEY (CODIGO_NOTA);

-- MEJORA SQL 1: la PK de NOTAS estaba comentada en el original.
ALTER TABLE NOTAS
  ADD CONSTRAINT NOT_KEY PRIMARY KEY (NUMERO, CODIGO_NOTA, FECHA);

ALTER TABLE NOTAS
  ADD CONSTRAINT N_E_KEY FOREIGN KEY (NUMERO) REFERENCES ESTUDIANTES (NUMERO);
ALTER TABLE NOTAS
  ADD CONSTRAINT N_D_KEY FOREIGN KEY (CODIGO_NOTA) REFERENCES CRITERIOS_DE_EVALUACION (CODIGO_NOTA);

ALTER TABLE ASISTENCIAS
  ADD CONSTRAINT A_E_KEY FOREIGN KEY (NUMERO) REFERENCES ESTUDIANTES (NUMERO);

