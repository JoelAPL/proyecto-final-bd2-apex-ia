-- ============================================================
-- VISTA V_ESTUDIANTES_PROY_FINAL
-- Faltaba en el Anexo II del grupo anterior pero la usan varios
-- queries de la aplicacion (Resumen General, Libro de Calificaciones).
-- La reconstruimos a partir de la formula de ponderacion que usan.
-- ============================================================
CREATE OR REPLACE VIEW V_ESTUDIANTES_PROY_FINAL AS
WITH prom AS (
    SELECT n.numero,
           ROUND(SUM(AVG(n.valor_nota) * c.porcentaje / c.cantidad) OVER (PARTITION BY n.numero), 2) AS prom_final
    FROM   notas n
    JOIN   criterios_de_evaluacion c ON c.codigo_nota = n.codigo_nota
    GROUP BY n.numero, c.codigo_nota, c.porcentaje, c.cantidad
),
asis AS (
    SELECT a.numero,
           ROUND(SUM(CASE WHEN a.status = 'S' THEN 1 ELSE 0 END) * 100 / COUNT(*), 2) AS por_asis
    FROM   asistencias a
    GROUP BY a.numero
)
SELECT DISTINCT
    e.numero,
    e.apellido,
    e.nombre,
    e.cedula,
    e.genero,
    p.nombre AS provincia,
    NVL(pr.prom_final, 0) AS prom_final,
    CASE
        WHEN NVL(pr.prom_final,0) >= 91 THEN 'A'
        WHEN NVL(pr.prom_final,0) >= 81 THEN 'B'
        WHEN NVL(pr.prom_final,0) >= 71 THEN 'C'
        WHEN NVL(pr.prom_final,0) >= 61 THEN 'D'
        ELSE 'F'
    END AS notas,
    NVL(a.por_asis, 0) AS por_asis
FROM  estudiantes e
LEFT JOIN provincias p ON p.id_provincia = e.id_provincia
LEFT JOIN prom pr      ON pr.numero = e.numero
LEFT JOIN asis a       ON a.numero  = e.numero;
